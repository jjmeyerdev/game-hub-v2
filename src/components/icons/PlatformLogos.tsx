// Platform logo component that renders the appropriate logo
interface PlatformLogoProps extends LogoProps {
  platform: 'steam' | 'playstation' | 'psn' | 'xbox' | 'epic' | 'nintendo' | 'gog' | 'ea' | 'battlenet' | 'battle.net' | 'ubisoft' | 'windows';
}

export function PlatformLogo({ platform, ...props }: PlatformLogoProps) {
  const normalizedPlatform = platform.toLowerCase().replace('.', '');

  switch (normalizedPlatform) {
    case 'steam':
      return <SteamLogo {...props} />;
    case 'playstation':
    case 'psn':
      return <PlayStationLogo {...props} />;
    case 'xbox':
      return <XboxLogo {...props} />;
    case 'epic':
      return <EpicLogo {...props} />;
    case 'nintendo':
      return <NintendoLogo {...props} />;
    case 'gog':
      return <GOGLogo {...props} />;
    case 'ea':
      return <EALogo {...props} />;
    case 'battlenet':
      return <BattleNetLogo {...props} />;
    case 'ubisoft':
      return <UbisoftLogo {...props} />;
    case 'windows':
      return <WindowsLogo {...props} />;
    default:
      return null;
  }
}

